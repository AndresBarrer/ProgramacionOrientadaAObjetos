public class Animal {
    String name;
    int age;
    boolean vaccinated;

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }
    public void setAge(int age) {
        this.age = age;
    }

    public boolean getVaccinated(){
        return vaccinated;
    }
    public void setVaccinated(boolean bool){
        vaccinated = bool;
    }

}
